package com.controlstatement.ass;

public class Value2 {
	public static void main(String args []) {
		
		int Day = 4;
		
		if(Day == 1) {
			System.out.println("Monday");
		} else if(Day == 2) {
			System.out.println("Tuesday");
		} else if(Day == 3) {
			System.out.println("Wed");
		} else {
			System.out.println("Not a valid day");
		}
	}
}
