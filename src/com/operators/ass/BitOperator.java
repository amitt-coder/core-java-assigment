package com.operators.ass;

public class BitOperator {
	public static void main(String[] args) {
		
		int a = 3;
		int b = 5;
		
		System.out.println(a|b);
		System.out.println(a&b);
		System.out.println(a^b);
		System.out.println(~b);
	
		System.out.println(a>>b);
		System.out.println(a<<b);
		System.out.println(a>>>b);
	
	}
}
