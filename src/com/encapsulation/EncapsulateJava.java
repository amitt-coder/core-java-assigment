package com.encapsulation;

public class EncapsulateJava {
	
	private int roll;
	private String name;
	
	public void setRollNo(int rollNo) {
		roll = rollNo;
		
	}
	
	public void setName(String name) {
		this.name = name;
	}
	public int getRollNo() {
		return roll;
	}
	public String getName() {
		return name;
	}
}
